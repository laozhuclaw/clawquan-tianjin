# ------------------------------------------------------------------
# Helpers — keep ORG_TREE compact while still emitting plain dicts
# (compatible with the existing _seed_org_recursive walker).
# ------------------------------------------------------------------

def _ent(name, short, industry, region, desc,
         agent_name, agent_icon, agent_desc, agent_tags):
    """Build an enterprise node with its representative agent."""
    return {
        "name": name, "short_name": short,
        "type": OrgType.ENTERPRISE,
        "industry": industry, "region": region, "description": desc,
        "agent": {
            "name": agent_name, "icon": agent_icon,
            "category": "企业代表",
            "description": agent_desc, "tags": agent_tags,
        },
    }


def _chamber(name, short, industry, region, desc,
             agent_name, agent_icon, agent_category, agent_desc, agent_tags,
             enterprises):
    """Build a chamber/association node with its agent and enterprise children."""
    return {
        "name": name, "short_name": short,
        "type": OrgType.CHAMBER,
        "industry": industry, "region": region, "description": desc,
        "agent": {
            "name": agent_name, "icon": agent_icon,
            "category": agent_category,
            "description": agent_desc, "tags": agent_tags,
        },
        "children": enterprises,
    }


